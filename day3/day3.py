#Data dalam variabel
nama = "Septa" 
tinggi_badan = 174
berat_badan = 62

#Mengecek tipe data dalam variabel
print(f'Tipe data dari data {nama} adalah {type(nama)}') # tipe data string (str)
print(f'Tipe data dari data {tinggi_badan} adalah {type(tinggi_badan)}') # tipe data integer (int)
print(f'Tipe data dari data {berat_badan} adalah {type(berat_badan)}') # tipe data float