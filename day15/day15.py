# List: Menyimpan nama mahasiswa
nama_mahasiswa = ["Septa", "Rendra", "Rossi"]

# Tuple: Menyimpan koordinat tempat duduk mahasiswa
koordinat_tempat_duduk = ((1, 2), (2, 3), (3, 4))

# Dictionary: Menyimpan nilai mahasiswa
nilai_mahasiswa = {"Septa": 85, "Rendra": 90, "Rossi": 95}

# Set: Menyimpan nomor identifikasi unik mahasiswa
id_mahasiswa = {101, 102, 103}

# Contoh fungsi untuk menampilkan data
def tampilkan_data():
    print("Nama Mahasiswa:", nama_mahasiswa)
    print("Koordinat Tempat Duduk:", koordinat_tempat_duduk)
    print("Nilai Mahasiswa:", nilai_mahasiswa)
    print("ID Mahasiswa:", id_mahasiswa)

# Panggil fungsi
tampilkan_data()
